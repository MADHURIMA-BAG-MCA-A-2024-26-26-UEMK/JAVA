public class AddNumbers {
    public static void main(String[] args) {
        // Declare two variables and assign values
        int num1 = 10;
        int num2 = 20;
        
        // Add the two numbers
        int sum = num1 + num2;
        
        // Display the result..
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
